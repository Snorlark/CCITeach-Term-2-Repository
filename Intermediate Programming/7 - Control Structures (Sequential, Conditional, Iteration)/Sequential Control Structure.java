// Sequential Control Example
public class SequentialExample {
    public static void main(String[] args) {
        System.out.println("Step 1: Starting the program");
        
        int number = 10;
        System.out.println("Step 2: Number is " + number);
        
        number = number + 5;
        System.out.println("Step 3: Added 5, number is now " + number);
        
        System.out.println("Step 4: Program complete");
    }
}